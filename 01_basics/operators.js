//operators//

//Arithmetic Operator//
let a = 10;
let b = 5;
addition = a + b;
console.log("Addition:", addition);

let c = 20;
let d = 4;
subtraction = c - d;
console.log("Subtraction:", subtraction);

let e = 6;
let f = 3;
multiplication = e * f;
console.log("Multiplication:", multiplication);

let g = 15;
let h = 3;
division = g / h;
console.log("Division:", division);

let i = 10;
let j = 3;
modulus = i % j;
console.log("Modulus:", modulus);

let k = 5;
let l = 10;
let comparison = k < l;
console.log("Comparison:", comparison);

let m = 7;
let n = 7;
let equality = m === n;
console.log("Equality:", equality);

//Logical Operators//

let o = 8;
let p = 12;
let logicalAnd = o < 10 && p > 5;
console.log("Logical AND:", logicalAnd);

let q = 15;
let r = 20;
let logicalOr = q < 10 || r > 15;
console.log("Logical OR:", logicalOr);

let s = 5;
let t = 10;
let logicalNot = !(s > t);
console.log("Logical NOT:", logicalNot);

//Increment or Decrement Operator//
let u = 10;
u++;
console.log("Increment:", u);

let v = 20;
v--;
console.log("Decrement:", v);

//Assignment Operator//
let w = 5;
w += 3;
console.log("Addition Assignment:", w);

let x = 10;
x -= 4;
console.log("Subtraction Assignment:", x);

let y = 6;
y *= 2;
console.log("Multiplication Assignment:", y);

let z = 15;
z /= 3;
console.log("Division Assignment:", z);
